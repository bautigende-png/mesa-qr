create extension if not exists "pgcrypto";

do $$
begin
  if not exists (select 1 from pg_type where typname = 'app_role') then
    create type public.app_role as enum ('ADMIN', 'WAITER');
  end if;

  if not exists (select 1 from pg_type where typname = 'event_action') then
    create type public.event_action as enum ('CALL_WAITER', 'REQUEST_BILL', 'VIEW_MENU');
  end if;

  if not exists (select 1 from pg_type where typname = 'event_status') then
    create type public.event_status as enum ('PENDING', 'ACKNOWLEDGED', 'RESOLVED');
  end if;
end $$;

do $$
begin
  if not exists (
    select 1
    from pg_enum
    where enumtypid = 'public.event_action'::regtype
      and enumlabel = 'VIEW_MENU'
  ) then
    alter type public.event_action add value 'VIEW_MENU';
  end if;
end $$;

create table if not exists public.profiles (
  id uuid primary key references auth.users(id) on delete cascade,
  email text not null unique,
  full_name text,
  role public.app_role not null default 'WAITER',
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now())
);

create table if not exists public.restaurant_tables (
  id uuid primary key default gen_random_uuid(),
  table_name text not null unique,
  active boolean not null default true,
  sector text,
  ordering_url text not null,
  menu_url_override text,
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now())
);

create table if not exists public.settings (
  id bigint primary key generated by default as identity,
  restaurant_name text not null,
  global_menu_url text,
  direct_order_url text,
  logo_url text,
  mobile_banner_url text,
  mobile_banner_text text,
  custom_message text,
  brand_primary_color text not null default '#0f172a',
  brand_secondary_color text not null default '#b45309',
  brand_tertiary_color text not null default '#f8f5ef',
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now())
);

create table if not exists public.events (
  id uuid primary key default gen_random_uuid(),
  table_id uuid not null references public.restaurant_tables(id) on delete cascade,
  action public.event_action not null,
  status public.event_status not null default 'PENDING',
  customer_email text,
  marketing_opt_in boolean not null default false,
  session_id uuid not null,
  acknowledged_at timestamptz,
  resolved_at timestamptz,
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now())
);

create index if not exists idx_events_status_created_at on public.events(status, created_at);
create index if not exists idx_events_table_session_action_created_at
  on public.events(table_id, session_id, action, created_at desc);
create index if not exists idx_events_created_at on public.events(created_at desc);

